package com.Object;



public class WebServiceObject {
	private int id;
	private String link;
	private String DateAdd;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getDateAdd() {
		return DateAdd;
	}
	public void setDateAdd(String dateAdd) {
		DateAdd = dateAdd;
	}	

}
